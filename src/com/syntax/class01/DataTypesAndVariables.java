package com.syntax.class01;

public class DataTypesAndVariables {

	public static void main(String[] args) {
		
		//primitive data types
		//numeric
		
		//type name = assign value
		
		byte variable1=127; //-128 to 127
		
		short variable2=128; 
		
		int anyName=1000;
		
		long variable3=100000;
		
		//to store floating numbers
		
		float var1=12.99f; 
		
		double var2=9.99999;
		
		//to represent yes or no
		boolean myVariable=true;
		boolean anotherVariable=false;
		
		//to represent a single character
		
		char myBox='C'; 
		char myBox2='&';
		char myBox3='7';

		
		System.out.println(myBox3);
		
	}

}
